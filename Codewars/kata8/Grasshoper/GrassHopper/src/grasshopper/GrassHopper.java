
package grasshopper;


public class GrassHopper {

    
    public static void main(String[] args) {
        
    }

    public static int summation(int n) {
      int i= 0;
      int contador = 0;
      while(i <= n) {
        
        contador+=i;
        i++;
        
        }
      return contador;
    }
}

